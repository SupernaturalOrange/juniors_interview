def sum_two_int_numbers(a: int, b: int) -> int:
    """
    Функция сложения двух целых чисел.
    :param a: Первое целое число для сложения (int).
    :param b: Второе целое число для сложения (int).
    :return: Сумма двух целых чисел (int).
    """
    return a + b


def area_of_the_rectangle(a: float, b: float) -> float:
    """
    Функция расчёта площади прямоугольника.
    :param a: Длина (float).
    :param b: Ширина (float).
    :return: Площадь прямоугольника (float).
    """
    return a * b
